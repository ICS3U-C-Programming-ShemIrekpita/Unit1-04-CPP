#include <iostream>
#include <cmath>

int main() {
    std::cout << "7 + 6 =" << 7 + 6 << std::endl;
    std::cout << "4 - 2 =" << 4 - 2 << std::endl;
    std::cout << "50/5 =" << 50 / 5 << std::endl;
    std::cout << "8-4/2 =" << 8 - 4 / 2 << std::endl;
    std::cout << "4^4 =" << pow(4, 4) << std::endl;
    std::cout << "1 / 3 =" << float(1) / float(3) << std::endl;
}
